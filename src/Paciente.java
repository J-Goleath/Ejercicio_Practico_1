
public class Paciente {
    
    private String nombre;
    private int turno;

    public Paciente() {
        this.nombre = "";
        this.turno = 0;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getTurno() {
        return turno;
    }

    public void setTurno(int turno) {
        this.turno = turno;
    }
    
    
    
    
    
}
